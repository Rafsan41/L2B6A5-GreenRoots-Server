import { prisma } from "../lib/prisma.js";

async function resolveSellerId(): Promise<string> {
  const email = process.env.SEED_SELLER_EMAIL
  if (email) {
    const seller = await prisma.user.findUnique({ where: { email } })
    if (seller) return seller.id
    console.warn(`⚠ SEED_SELLER_EMAIL "${email}" not found in DB, falling back to first SELLER`)
  }
  const first = await prisma.user.findFirst({ where: { role: "SELLER" }, orderBy: { createdAt: "asc" } })
  if (first) return first.id
  throw new Error("No SELLER account found. Create a seller account first, then re-run the seed.")
}

