// ── Main seed function ────────────────────────────────────────────────────────
async function seedMedicines() {
  console.log("🌱 Starting seed...\n")

  const SELLER_ID = await resolveSellerId()
  console.log(`👤 Using seller ID: ${SELLER_ID}\n`)

  // Step 1: Upsert categories
  console.log("📂 Upserting categories...")
  const categoryMap = new Map<string, string>()

  for (const cat of categories) {
    const result = await prisma.category.upsert({
      where: { slug: cat.slug },
      create: cat,
      update: {},
    })
    categoryMap.set(cat.slug, result.id)
    console.log(`   ✓ ${cat.name}`)
  }

  // Step 2: Upsert medicines
  console.log("\n💊 Upserting medicines...")
  let created = 0
  let skipped = 0

  for (const med of medicines) {
    const categoryId = categoryMap.get(med.categorySlug)
    if (!categoryId) {
      console.warn(`   ⚠ No category found for slug "${med.categorySlug}", skipping ${med.name}`)
      skipped++
      continue
    }

    const { categorySlug, ...medData } = med

    await prisma.medicine.upsert({
      where: { slug: med.slug },
      create: { ...medData, categoryId, sellerId: SELLER_ID },
      update: {},
    })

    console.log(`   ✓ ${med.name}`)
    created++
  }

  console.log(`\n✅ Seed complete! ${created} medicines upserted, ${skipped} skipped.`)
}

seedMedicines()
  .catch((e) => { console.error("❌ Seed failed:", e); process.exit(1) })
  .finally(() => prisma.$disconnect())
